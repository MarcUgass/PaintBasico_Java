    private MumLienzo getSelectedLienzo() {
        VentanaInterna vi;
        vi = (VentanaInterna) escritorio.getSelectedFrame();
        return vi != null ? vi.getLienzo1() : null;
    }

    private void nuevo_menuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nuevo_menuActionPerformed
    VentanaInterna vi = new VentanaInterna();
    escritorio.add(vi);
    vi.setVisible(true);
    
    //Crear objeto manejador y enlazarlo con la funcion manejadora
    vi.addInternalFrameListener( new ManejadorVentanaInterna());
    
    BufferedImage img;
    img = new BufferedImage(300,300,BufferedImage.TYPE_INT_RGB);
    vi.getLienzo1().setImage(img);
    }//GEN-LAST:event_nuevo_menuActionPerformed

