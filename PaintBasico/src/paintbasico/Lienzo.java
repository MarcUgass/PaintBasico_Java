    private void formMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMousePressed
        punto = evt.getPoint();
        switch(formaSeleccionada) {
            case LINEA -> forma = new Line2D.Float(evt.getPoint(), evt.getPoint());
            case RECTANGULO -> forma = new Rectangle2D.Float();
            case ELIPSE -> forma = new Ellipse2D.Float();
        }
    }//GEN-LAST:event_formMousePressed

    private void formMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseDragged
        switch(formaSeleccionada) {
            case LINEA -> ((Line2D)forma).setLine(((Line2D)forma).getP1(), evt.getPoint());
            case RECTANGULO -> ((Rectangle2D)forma).setFrameFromDiagonal(punto, evt.getPoint());
            case ELIPSE -> ((Ellipse2D)forma).setFrameFromDiagonal(punto, evt.getPoint());
        }
        this.repaint();
    }//GEN-LAST:event_formMouseDragged


