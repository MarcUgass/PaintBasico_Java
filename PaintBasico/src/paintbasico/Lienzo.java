    @Override
    public void paint(Graphics g){
    super.paint(g);
    Graphics2D g2d = (Graphics2D)g;
    g2d.setPaint(color);
    if (this.getRelleno()== true)
    {
        g2d.fill(forma);
    }
    g2d.draw(forma);
    }

